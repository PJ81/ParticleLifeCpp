class cRand {

//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++ functions
public:
	float									randF											(void);
	float									randF											(float x);
	float									randF											(float h, float l);
};
//--------------------------------------------------------------------------------------------------
inline float cRand::randF() {
	return static_cast <float> (rand()) / static_cast <float> (RAND_MAX);
}
//--------------------------------------------------------------------------------------------------
inline float cRand::randF(float x) {
	return static_cast <float> (rand()) / (static_cast <float> (RAND_MAX / x));
}
//--------------------------------------------------------------------------------------------------
inline float cRand::randF(float h, float l) {
	return l + static_cast <float> (rand()) / (static_cast <float> (RAND_MAX / (h - l)));
}
